// Current active language (default English)
let CURRENT_LANG = localStorage.getItem('rowel_lang') || 'en';

/**
 * Get a translation string by key, with optional placeholder replacements.
 * @param {string} key - dot-notation translation key
 * @param {Object} [vars] - placeholder values e.g. {xp: 50}
 * @returns {string}
 */
function t(key, vars) {
  const dict = TRANSLATIONS[CURRENT_LANG] || TRANSLATIONS.en;
  let str = dict[key] || TRANSLATIONS.en[key] || key;
  if (vars) {
    Object.keys(vars).forEach(k => {
      str = str.replace(`{${k}}`, vars[k]);
    });
  }
  return str;
}

/**
 * Apply translations to all elements with data-i18n / data-i18n-placeholder attributes.
 */
function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    el.setAttribute('placeholder', t(key));
  });

  // Handle RTL for Arabic
  if (CURRENT_LANG === 'ar') {
    document.documentElement.setAttribute('dir', 'rtl');
    document.documentElement.setAttribute('lang', 'ar');
  } else {
    document.documentElement.setAttribute('dir', 'ltr');
    document.documentElement.setAttribute('lang', CURRENT_LANG);
  }
}

/**
 * Switch the active language and persist the choice.
 * @param {string} lang - 'en' | 'ar' | 'fr' | 'es'
 */
function setLanguage(lang) {
  if (!TRANSLATIONS[lang]) return;
  CURRENT_LANG = lang;
  localStorage.setItem('rowel_lang', lang);
  applyTranslations();

  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // Re-render dynamic content that depends on language
  if (typeof renderMissions === 'function') renderMissions();
  if (typeof renderFeed === 'function') renderFeed();
  if (typeof renderAnalytics === 'function') renderAnalytics();
  if (typeof renderSocialContent === 'function') renderSocialContent();
}
